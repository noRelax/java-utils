package com.itstar.erp.vo.supply;

public class SupplyBean {
	private int spID;
	private String spName;
	private String spAddress;
	private String spPhone;
	private String spZip;
	private String spConn;
	private String spConnPhone;
	private String spEmail;
	public int getSpID() {
		return spID;
	}
	public void setSpID(int spID) {
		this.spID = spID;
	}
	public String getSpName() {
		return spName;
	}
	public void setSpName(String spName) {
		this.spName = spName;
	}
	public String getSpAddress() {
		return spAddress;
	}
	public void setSpAddress(String spAddress) {
		this.spAddress = spAddress;
	}
	public String getSpPhone() {
		return spPhone;
	}
	public void setSpPhone(String spPhone) {
		this.spPhone = spPhone;
	}
	public String getSpZip() {
		return spZip;
	}
	public void setSpZip(String spZip) {
		this.spZip = spZip;
	}
	public String getSpConn() {
		return spConn;
	}
	public void setSpConn(String spConn) {
		this.spConn = spConn;
	}
	public String getSpConnPhone() {
		return spConnPhone;
	}
	public void setSpConnPhone(String spConnPhone) {
		this.spConnPhone = spConnPhone;
	}
	public String getSpEmail() {
		return spEmail;
	}
	public void setSpEmail(String spEmail) {
		this.spEmail = spEmail;
	}
	
	
	
}
